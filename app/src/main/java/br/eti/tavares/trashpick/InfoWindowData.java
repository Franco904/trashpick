package br.eti.tavares.trashpick;

public class InfoWindowData {
  private String nome_lixo;
  private String detalhes_lixo;
  private String imagem;

  public String getNome_lixo() {
    return nome_lixo;
  }

  public void setNome_lixo(String nome_lixo) {
    this.nome_lixo = nome_lixo;
  }

  public String getDetalhes_lixo() {
    return detalhes_lixo;
  }

  public void setDetalhes_lixo(String detalhes_lixo) {
    this.detalhes_lixo = detalhes_lixo;
  }

  public String getImagem() {
    return imagem;
  }

  public void setImagem(String imagem) {
    this.imagem = imagem;
  }
}